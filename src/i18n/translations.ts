export const getTranslation = (lang: Language, key: string) => {
  const keys = key.split('.');
  let value: any = translations;

  for (const k of keys) {
    if (value[k] === undefined) return key;
    value = value[k];
  }

  if (typeof value === 'object' && value[lang] !== undefined) {
    return value[lang];
  }

  return key;
};
